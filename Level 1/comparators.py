# >=
print(2 >= 2) # Prints True
print(2 >= 3) # Prints False
print(3 >= 2) # Prints False

# <=
print(2 <= 2) # Prints True
print(2 <= 3) # Prints True
print(3 <= 2) # Prints False

# == (Equal to)
print(2 == 2) # Prints True
print(2 == 3) # Prints False
print(3 == 2) # Prints False

# !=
print(2 != 2) # Prints False
print(2 != 3) # Prints True
print(3 != 2) # Prints True

print("Hello" == "Hello") # Prints True
print("Hello" == "hello") # Prints False
